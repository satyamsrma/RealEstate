      async function main() {
        // Checking if the Ethereum provider (MetaMask) is available
        if (typeof window.ethereum !== "undefined") {
          // Requesting MetaMask to enable accounts
          await window.ethereum.enable();

          const accounts = await window.ethereum.request({
            method: "eth_accounts",
          });
          const userAccount = accounts[0];

          // Creating an instance of the contract
          const web3 = new Web3(window.ethereum); // Create a web3 instance
          const contract = new web3.eth.Contract(contractABI, contractAddress);

          const openContract = new web3.eth.Contract(contABI, contAddr);

          // Adding event listeners to buttons
          document
            .getElementById("mintButton")
            .addEventListener("click", () =>
              mintNFT(contract, openContract, userAccount)
            );
          document
            .getElementById("mintButton")
            .addEventListener("click", () =>
              mintNFT(contract, openContract, userAccount)
            );
          document
            .getElementById("createAgreementButton")
            .addEventListener("click", () =>
              createAgreement(contract, userAccount)
            );
          document
            .getElementById("executeAgreementButton")
            .addEventListener("click", () =>
              executeAgreement(contract, userAccount)
            );
        } else {
          console.log(
            "MetaMask not detected. Please install the MetaMask extension."
          );
        }
      }

